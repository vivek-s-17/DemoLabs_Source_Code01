console.info("------------ DEMO: Strict Equality Operator `===`");


let testLet;                     // declared but not assigned. So it is "undefined"

if (testLet === undefined) {
    console.log("testLet is undefined");
} else {
    console.log("testLet is defined");
}


var testVar;                     // declared but not assigned. So it is "undefined"

if (testVar === undefined) {
    console.log("testVar is undefined");
} else {
    console.log("testVar is defined");
}
